
public class MiniProjeSesliHarf {

	public static void main(String[] args) {
		//kalın sesli-ince sesli harfler
		char harf ='A';
		
		switch (harf) {
		  case 'A':
		  case 'I':
		  case 'O':
		  case 'U':
			  System.out.println("kalın sesli harfler");
			  break;
			  default:
			  System.out.println("ince sesli harfler");

		}
	}

}
